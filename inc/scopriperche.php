    <p><b>Che cosa significa essere volontario o volontaria?</b><br/>

Essere volontario o volontaria significa mettere il proprio tempo e le proprie energie al servizio degli altri, senza alcuna costrizione e senza desiderare nulla in cambio.<br/>
Per alcuni, fare volontariato è una scelta di vita, per altri una vocazione, per altri ancora un modo per dare qualcosa, non solo agli altri, ma soprattutto a se stessi.<br/>
Essere volontario o volontaria significa non solo lavorare per le persone, ma con le persone, all'interno della propria comunità. Significa mettere a frutto le proprie competenze per migliorare le condizioni di vita delle persone più vulnerabili, chiunque esse siano, ed essere consapevoli che, per tanto che ciò che facciamo possa sembrarci poco, stiamo facendo la differenza. Nella loro vita come nella nostra.<br/><br/>
 
<b>Il volontariato in Croce Rossa</b><br/>

E' questo che fanno ogni giorno i volontari della Croce Rossa Italiana nell'aiuto alle persone più vulnerabili: nel rispetto dei Sette Principi di Croce Rossa e Mezzaluna Rossa, lavorano negli ospedali, nelle case riposo, nei centri di accoglienza per migranti, sulle ambulanze, nei centri sportivi, nelle scuole e in situazioni di emergenza. I volontari contribuiscono a difendere la salute e la vita, a diffondere i principi e valori di Croce Rossa e Mezzaluna Rossa e a formare cittadini responsabili di oggi e di domani.<br/><br/>

